// dts-generator.js
//
// Scan all .rs files in src-native/src/ and generate a combined rvst.d.ts
// that provides TypeScript autocompletion for rvst.* calls.

import { readFileSync, writeFileSync, existsSync, readdirSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';
import { createHash } from 'node:crypto';
import {
  extractRustFunctions,
  extractRustStructs,
  rustTypeToTs,
} from '../../rvst-api/src/codegen/ts_from_rust.js';

/**
 * Convert snake_case to camelCase.
 * @param {string} s
 * @returns {string}
 */
function toCamelCase(s) {
  return s.replace(/_([a-z])/g, (_, c) => c.toUpperCase());
}

/**
 * Scan all .rs files in src-native/src/ and produce a combined rvst.d.ts string.
 * Returns null if no native source directory exists.
 * @param {string} projectRoot
 * @returns {string | null}
 */
export function generateDts(projectRoot) {
  const nativeSrcDir = join(projectRoot, 'src-native', 'src');
  if (!existsSync(nativeSrcDir)) return null;

  const rsFiles = readdirSync(nativeSrcDir).filter(
    (f) => f.endsWith('.rs') && f !== 'lib.rs'
  );

  if (rsFiles.length === 0) return null;

  /** @type {ReturnType<typeof extractRustFunctions>} */
  const allFunctions = [];
  /** @type {ReturnType<typeof extractRustStructs>} */
  const allStructs = [];

  for (const file of rsFiles) {
    const content = readFileSync(join(nativeSrcDir, file), 'utf8');
    allFunctions.push(...extractRustFunctions(content));
    allStructs.push(...extractRustStructs(content));
  }

  if (allFunctions.length === 0 && allStructs.length === 0) return null;

  return buildDeclarationFile(allFunctions, allStructs);
}

/**
 * Build the full .d.ts content from extracted functions and structs.
 * @param {ReturnType<typeof extractRustFunctions>} functions
 * @param {ReturnType<typeof extractRustStructs>} structs
 * @returns {string}
 */
function buildDeclarationFile(functions, structs) {
  let output = '// Auto-generated by @rvst/vite-plugin — do not edit\n';
  output += '// Generated from src-native/ Rust source files\n\n';

  // Interfaces for structs
  for (const s of structs) {
    output += `export interface ${s.name} {\n`;
    for (const f of s.fields) {
      const tsType = rustTypeToTs(f.type);
      output += `  ${toCamelCase(f.name)}${f.optional ? '?' : ''}: ${tsType};\n`;
    }
    output += '}\n\n';
  }

  // Global rvst namespace (commands become Promise-returning, watches become void)
  if (functions.length > 0) {
    output += 'declare global {\n';
    output += '  namespace rvst {\n';
    for (const fn of functions) {
      const params = fn.params
        .filter((p) => p.name !== 'self' && !p.type.includes('impl Fn'))
        .map((p) => `${toCamelCase(p.name)}: ${rustTypeToTs(p.type)}`)
        .join(', ');
      const ret = rustTypeToTs(fn.returnType);
      const tsName = toCamelCase(fn.name);

      if (fn.annotation === 'command') {
        output += `    function ${tsName}(${params}): Promise<${ret}>;\n`;
      } else if (fn.annotation === 'watch') {
        output += `    function ${tsName}(${params}): AsyncIterable<${ret}>;\n`;
      }
    }
    output += '  }\n';
    output += '}\n\n';
    output += 'export {};\n';
  }

  return output;
}

/**
 * Write rvst.d.ts into the project's src/ directory, but only if content changed.
 * Returns true if the file was written.
 * @param {string} projectRoot
 * @returns {boolean}
 */
export function writeDtsIfChanged(projectRoot) {
  const content = generateDts(projectRoot);
  if (!content) return false;

  const srcDir = join(projectRoot, 'src');
  mkdirSync(srcDir, { recursive: true });

  const dtsPath = join(srcDir, 'rvst.d.ts');
  const hash = createHash('sha256').update(content).digest('hex').slice(0, 16);

  // Check existing content hash to avoid unnecessary writes / HMR triggers
  try {
    const existing = readFileSync(dtsPath, 'utf8');
    const existingHash = createHash('sha256').update(existing).digest('hex').slice(0, 16);
    if (existingHash === hash) return false;
  } catch {
    // File doesn't exist yet
  }

  writeFileSync(dtsPath, content);
  return true;
}
